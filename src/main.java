public class main {
    public static void main(String[] args){

        byte bOne = 17;
        short sOne = 67;
        int iNum = 54;
        long lNum = 2157684L;
        float fNum = 2.6f;
        double dNum = 1.2;
        boolean bFlag = false;
        char cDigit = 'b';

        System.out.println("byte example:  " + sOne);
        System.out.println("short example:  " + bOne);
        System.out.println("int example:  " + iNum);
        System.out.println("long example:  " + lNum);
        System.out.println("float example:  " + fNum);
        System.out.println("double example:  " + dNum);
        System.out.println("boolean example:  " + bFlag);
        System.out.println("char example:  " + cDigit);


    }
}
